class ForExample
{
	public static void main(String[] args)
	 {
		// A gto Z

		for (char ch ='A';ch<='Z' ;ch++ )
		 {
			System.out.print(ch +" ");
		}

		System.out.println();
		System.out.println("-------------------");

		for (char ch = 97; ch <=122 ;ch++)
		 {
			System.out.print(ch+" ");
		}

		System.out.println();
		System.out.print(ch+" ");

		System.out.println("---------------------");

		for (char ch=47;ch<=57 ;ch++ ) 
		{
			System.out.print(ch+" ");	
		}

	}
}